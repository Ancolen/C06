#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	int		k;

	k = 1;
	i = 0;
	k = ac - 1;
	while (k > 0)
	{
		i = 0;
		while (av[k][i])
		{
			write(1, &av[k][i], 1);
			i++;
		}
		write(1, "\n", 1);
		k--;
	}
}
